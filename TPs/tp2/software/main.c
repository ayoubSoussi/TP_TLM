/* Must come after stdio.h to be able to modify libc functions
 * (printf, rand) after they are defined */
#include "hal.h"

#define ALIGN(addr)				\
	((addr) & (-sizeof(uint32_t)))

#define BIT(bit)				\
	(1 << (bit))

#define TEST_BIT(var, bit)			\
	((var) & BIT(bit))

#define SET_BIT(var, bit)			\
	do { (var) |= BIT(bit); } while(0)

#define CLEAR_BIT(var, bit)			\
	do { (var) &= ~BIT(bit); } while(0)

int get_pixel(uint32_t base_addr, int x, int y)
{
	uint32_t addr = base_addr + ALIGN((x / CHAR_BIT) + (y * (VGA_WIDTH / CHAR_BIT)));
	int bit = 31 - x % (sizeof(uint32_t) * CHAR_BIT);

	uint32_t data = hal_read32(addr);
	return (TEST_BIT(data, bit) != 0);
}

void static inline set_pixel(uint32_t base_addr, uint32_t x, uint32_t y, uint32_t value)
{
	uint32_t addr = base_addr + ALIGN((x / CHAR_BIT) + (y * (VGA_WIDTH / CHAR_BIT)));
	uint32_t bit = 31 - x % (sizeof(uint32_t) * CHAR_BIT);

	uint32_t data = hal_read32(addr);
	if (value)
		SET_BIT(data, bit);
	else
		CLEAR_BIT(data, bit);
	hal_write32(addr, data);
}

void static inline clr_screen(uint32_t base_addr)
{
	uint32_t nbytes = (VGA_WIDTH * VGA_HEIGHT) / CHAR_BIT;
	for (uint32_t i = 0; i < nbytes; i += sizeof(uint32_t)) {
		hal_write32(base_addr + i, 0);
	}
}

/*
 * Double buffering principle, to avoid tearing and scratches during display:
 *   - do the rendering into back_buffer
 *   - display the front buffer
 *   - swap the pointer on vsync
 */
static uint32_t front_buffer_addr;
static uint32_t back_buffer_addr;

static void skullcopy(uint32_t base_addr)
{
	uint32_t nwords = (VGA_WIDTH * VGA_HEIGHT) / CHAR_BIT;
	for (uint32_t i = 0; i < nwords; i += sizeof(uint32_t)) {
		hal_write32(base_addr + i, skull_bits[i>>2]);
	}
}

/*
 * Copying back buffer to front buffer.
 * To be done during porch indicated by the vga interruption
 * MVGA always reads from front buffer
 * Drawing is always done in back buffer
 */
static void vramcpy(void)
{
	uint32_t nwords = (VGA_WIDTH * VGA_HEIGHT) / CHAR_BIT;
	for (uint32_t i = 0; i < nwords; i += sizeof(uint32_t)) {
		hal_write32(front_buffer_addr + i, hal_read32(back_buffer_addr + i));
	}
}

/*
 * Draw a bouncing ball.
 * The ball moves at the speed of the vga interrupt, and changes speed and angles on each timer interrupt
 */
static int32_t x, y, dx, dy;
static uint32_t sz;

void draw_ball(uint32_t init)
{
	if (!init) {
		/* Erase old position */
		for (uint32_t k = 0; k < sz; k++)
			for (uint32_t l = 0; l < sz; l++)
				set_pixel(back_buffer_addr, x + k, y + l, 1^get_pixel(back_buffer_addr, x + k, y + l));

		/* Move the ball */
		x += dx;
		y += dy;

		/* Are we bouncing or just advancing? */
		if (x + sz >= VGA_WIDTH) {
			x = VGA_WIDTH - sz;
			dx *= -1;
		}

		if (x < 0) {
			x = 0;
			dx *= -1;
		}

		if (y + sz >= VGA_HEIGHT) {
			y = VGA_HEIGHT - sz;
			dy *= -1;
		}

		if (y < 0) {
			y = 0;
			dy *= -1;
		}
	}

	/* Draw the new ball */
	for (uint32_t k = 0; k < sz; k++)
		for (uint32_t l = 0; l < sz; l++)
			set_pixel(back_buffer_addr, x + k, y + l, 1^get_pixel(back_buffer_addr, x + k, y + l));
}

int main(void)
{
	printf("-- boot complete --\r\n");
	/* Program timer: period (must come early) */
	hal_write32(TIMER_BASEADDR + TIMER_0_TLR_OFFSET, 0x08000000);

	/* Configure and launch timer */
	hal_write32(TIMER_BASEADDR + TIMER_0_CSR_OFFSET,
		  BIT(TIMER_TINT) | BIT(TIMER_ENT) | BIT(TIMER_ENIT) |
		  BIT(TIMER_ARHT) | BIT(TIMER_UDT));

	/*
	 * Video ram
	 */
	back_buffer_addr = 0x80020000 - 1024 - (VGA_WIDTH * VGA_HEIGHT) / CHAR_BIT;
	front_buffer_addr = back_buffer_addr - (VGA_WIDTH * VGA_HEIGHT) / CHAR_BIT;

	/*
	 * Sets the drawing and displaying landscapes
	 */
	skullcopy(front_buffer_addr);
	skullcopy(back_buffer_addr);

	/* start vga */
	hal_write32(VGA_BASEADDR + VGA_CFG_OFFSET, front_buffer_addr);

	/* program gpio to input */
	hal_write32(GPIO_BASEADDR + GPIO_TRI_OFFSET, GPIO_INPUT);

	/* Enable and acknowledge all interrupts */
	hal_write32(INTC_BASEADDR + XIN_MER_OFFSET, ~0);
	hal_write32(INTC_BASEADDR + XIN_IER_OFFSET, ~0);
	hal_write32(INTC_BASEADDR + XIN_IAR_OFFSET, ~0);
	enable_interrupts();

	/* Setting ball parameters */
	x  = VGA_WIDTH/2;
	y  = VGA_HEIGHT/2;
	dx = prand()%10 + 1;
	dy = prand()%10 + 1;
	sz = prand()%10 + 1;

	draw_ball(1);

	printf("-- entering main loop --\r\n");
	while (1) {
		while (1) {
			uint32_t d = hal_read32(GPIO_BASEADDR + GPIO_DATA_OFFSET);
			if (TEST_BIT(d, GPIO_BTN0)) {
				break;
			}
		}
	}

	return 0;
}

void vga_isr(void)
{
	draw_ball(0);
#if 0
	printf("vga_isr: displaying front buffer\r\n");
#else
	printf(".");
#endif
	vramcpy();
}

void timer_0_isr(void)
{
	uint32_t a, b, sx, sy;
	printf("\r\ntimer_0_isr: drawing ball\r\n");

	a  = prand()&0x7;
	b  = prand()&0x7;
	sx = prand()&1;
	sy = prand()&1;

	dx = sx ? dx - a : dx + a;
	dy = sy ? dy - b : dy + b;
}

void timer_1_isr() {
	printf("timer_1_isr\r\n");
}

void interrupt_handler() {
	static int count;
	irq_received = 1;
	count++;

	uint32_t status = hal_read32(INTC_BASEADDR + XIN_ISR_OFFSET);

	/* check if vga requested interrupt */
	uint32_t vga_irq = hal_read32(VGA_BASEADDR + VGA_INT_OFFSET);
	if (vga_irq) {
		/* call the service routine */
		vga_isr();
		/* clear interrupt */
		hal_write32(VGA_BASEADDR + VGA_INT_OFFSET, vga_irq);
	}

	/* check if timer 0 requested interrupt */
	uint32_t timer_0_csr = hal_read32(TIMER_BASEADDR + TIMER_0_CSR_OFFSET);
	if (timer_0_csr & TIMER_INTERRUPT) {
		/* call the service routine */
		timer_0_isr();
		/* clear the timer interrupt */
		hal_write32(TIMER_BASEADDR + TIMER_0_CSR_OFFSET, timer_0_csr);
	}

	uint32_t timer_1_csr = hal_read32(TIMER_BASEADDR + TIMER_1_CSR_OFFSET);
	if (timer_1_csr & TIMER_INTERRUPT) {
		/* call the service routine */
		timer_1_isr();
		/* clear the timer interrupt */
		hal_write32(TIMER_BASEADDR + TIMER_1_CSR_OFFSET, timer_1_csr);
	}

	/* TLM's UART has no interrupts -> Nothing here. The Zybo
	 * implementation has no UART but uses a dedicated debug
	 * module. */

	// ACK for interrupt controler
	hal_write32(INTC_BASEADDR + XIN_IAR_OFFSET, status);
}
